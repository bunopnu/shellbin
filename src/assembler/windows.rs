/// A struct representing a Windows-specific assembler.
pub struct WindowsAssembler;

impl Assembler for WindowsAssembler {
    fn assemble(source: String) -> Result<Vec<u8>, super::Error> {
        let length = source.len();

        if length > PREFFERED_MAXIMUM {
            return Err(super::Error::TooLong);
        }

        let data = source.as_bytes().to_vec();

        let mut binary = EXECUTABLE.to_vec();
        binary.extend(data);

        // Add padding zeros to the binary vector to reach the absolute maximum size
        binary.extend(std::iter::repeat(0).take(ABSOLUTE_MAXIMUM - length));

        Ok(binary)
    }
}
